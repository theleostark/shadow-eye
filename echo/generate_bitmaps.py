#!/usr/bin/env python3
"""
Generate ECHO-branded bitmap C header files for the ECHO firmware.

Replaces TRMNL logo bitmaps with ECHO wordmark bitmaps, encoded in
bb_epaper Group5 (G5) compressed format with the BB_BITMAP header.

Usage:
    python3 echo/generate_bitmaps.py

Dependencies:
    pip install Pillow qrcode
"""

import struct
import os
from pathlib import Path

from PIL import Image, ImageDraw, ImageFont

try:
    import qrcode
except ImportError:
    print("ERROR: qrcode not installed. Run: pip install qrcode")
    raise

# ---------------------------------------------------------------------------
# Configuration
# ---------------------------------------------------------------------------

SCRIPT_DIR = Path(__file__).parent
OUTPUT_DIR = SCRIPT_DIR / "generated"

# Bitmap specs matching originals
BITMAPS = {
    "logo_big":    {"width": 640, "height": 640, "var": "logo_big",    "file": "logo_big.h"},
    "logo_medium": {"width": 240, "height": 240, "var": "logo_medium", "file": "logo_medium.h"},
    "logo_small":  {"width": 86,  "height": 86,  "var": "logo_small",  "file": "logo_small.h"},
}

QR_SPEC = {
    "width": 66, "height": 66,
    "var": "wifi_failed_qr",
    "file": "wifi_failed_qr.h",
    "url": "https://shadowlab.cc/echo",
}

HEADER_COMMENT = "// Generated by echo/generate_bitmaps.py — ECHO 0.0.1-alpha"

# BB_BITMAP marker (little-endian 0xBBBF)
BB_BITMAP_MARKER = 0xBBBF

# ---------------------------------------------------------------------------
# G5 Encoder (Python port of Larry Bank's g5enc.inl)
# ---------------------------------------------------------------------------

# Number of consecutive 1-bits from MSB in a byte
_bitcount = [0]*256
for _i in range(256):
    _c = 0
    for _b in range(7, -1, -1):
        if _i & (1 << _b):
            _c += 1
        else:
            break
    _bitcount[_i] = _c

# Vertical code table: (code, length) for V(-3)..V(+3)
_vtable = [
    (3, 7),   # V(-3) = 0000011
    (3, 6),   # V(-2) = 000011
    (3, 3),   # V(-1) = 011
    (1, 1),   # V(0)  = 1
    (2, 3),   # V(1)  = 010
    (2, 6),   # V(2)  = 000010
    (2, 7),   # V(3)  = 0000010
]

HORIZ_SHORT_SHORT = 0
HORIZ_SHORT_LONG = 1
HORIZ_LONG_SHORT = 2
HORIZ_LONG_LONG = 3

MAX_IMAGE_FLIPS = 512


class G5BitWriter:
    """Buffered bit writer matching G5ENCInsertCode / G5ENCFlushBits."""
    def __init__(self):
        self.buf = bytearray()
        self.bits = 0        # 32-bit accumulator
        self.bit_off = 0     # bits used in accumulator

    def insert(self, code: int, length: int):
        if (self.bit_off + length) > 32:
            self.bits |= (code >> (self.bit_off + length - 32)) & 0xFFFFFFFF
            # Write 4 bytes big-endian
            self.buf.extend(self.bits.to_bytes(4, 'big'))
            self.bits = (code << (64 - self.bit_off - length)) & 0xFFFFFFFF
            self.bit_off = self.bit_off + length - 32
        else:
            self.bits |= (code << (32 - self.bit_off - length)) & 0xFFFFFFFF
            self.bit_off += length

    def flush(self):
        while self.bit_off >= 8:
            self.buf.append((self.bits >> 24) & 0xFF)
            self.bits = (self.bits << 8) & 0xFFFFFFFF
            self.bit_off -= 8
        if self.bit_off:
            self.buf.append((self.bits >> 24) & 0xFF)
        self.bit_off = 0
        self.bits = 0


def _encode_line_to_flips(pixel_bytes: bytes, xsize: int) -> list:
    """Convert a line of 1-bpp MSB-first pixels to run-end positions."""
    flips = []
    byte_count = (xsize + 7) >> 3
    idx = 0
    c = pixel_bytes[idx] if idx < len(pixel_bytes) else 0
    idx += 1
    byte_count -= 1
    cbits = 8
    run_len = 0
    x = 0
    xborder = xsize

    while byte_count >= 0:
        if len(flips) >= MAX_IMAGE_FLIPS - 4:
            break
        # Count white (1-bits from MSB)
        i = _bitcount[c & 0xFF]
        run_len += i
        c = (c << i) & 0xFF
        cbits -= i
        if cbits <= 0:
            run_len += cbits
            cbits = 8
            if byte_count > 0:
                c = pixel_bytes[idx] if idx < len(pixel_bytes) else 0
                idx += 1
            else:
                c = 0
            byte_count -= 1
            continue
        # Flip to count black
        c = (~c) & 0xFF
        # Store white run end
        xborder -= run_len
        if xborder < 0:
            run_len += xborder
            break
        x += run_len
        flips.append(x)
        run_len = 0

        # Count black (which are now 1-bits after flip)
        while True:
            i = _bitcount[c & 0xFF]
            run_len += i
            c = (c << i) & 0xFF
            cbits -= i
            if cbits <= 0:
                run_len += cbits
                cbits = 8
                if byte_count > 0:
                    c = pixel_bytes[idx] if idx < len(pixel_bytes) else 0
                    idx += 1
                    c = (~c) & 0xFF
                else:
                    c = 0
                byte_count -= 1
                if byte_count < 0:
                    break
                continue
            break

        # Flip back to white
        c = (~c) & 0xFF
        # Store black run end
        xborder -= run_len
        if xborder < 0:
            run_len += xborder
            break
        x += run_len
        flips.append(x)
        run_len = 0

    # Terminate with multiple xsize entries
    for _ in range(4):
        flips.append(xsize)

    return flips


def g5_encode(pixel_rows: list, width: int, height: int) -> bytes:
    """
    Encode a 1-bpp image using G5 compression.

    pixel_rows: list of bytes objects, one per row, MSB-first 1-bpp.
                White pixels = 1, Black pixels = 0.
    Returns: compressed G5 data (without the 8-byte BB_BITMAP header).
    """
    iHLen = (width - 1).bit_length()  # bits needed for longest run

    bw = G5BitWriter()

    # Reference line starts as all-white (no flips, just xsize markers)
    ref_flips = [width] * MAX_IMAGE_FLIPS

    for y in range(height):
        cur_flips = _encode_line_to_flips(pixel_rows[y], width)

        # Pad cur_flips if needed
        while len(cur_flips) < 8:
            cur_flips.append(width)

        # G5 encode this line
        a0 = 0
        iCur = 0
        iRef = 0

        while a0 < width:
            b1 = ref_flips[iRef] if iRef < len(ref_flips) else width
            b2 = ref_flips[iRef + 1] if (iRef + 1) < len(ref_flips) else width
            a1 = cur_flips[iCur] if iCur < len(cur_flips) else width

            if b2 < a1:
                # Pass mode
                a0 = b2
                iRef += 2
                bw.insert(1, 4)  # Pass code = 0001
            else:
                dx = b1 - a1
                if dx > 3 or dx < -3:
                    # Horizontal mode
                    bw.insert(1, 3)  # Horizontal code = 001
                    run1 = cur_flips[iCur] - a0
                    a2 = cur_flips[iCur + 1] if (iCur + 1) < len(cur_flips) else width
                    run2 = a2 - cur_flips[iCur]

                    if run1 < 8:
                        if run2 < 8:
                            bw.insert(HORIZ_SHORT_SHORT, 2)
                            bw.insert(run1, 3)
                            bw.insert(run2, 3)
                        else:
                            bw.insert(HORIZ_SHORT_LONG, 2)
                            bw.insert(run1, 3)
                            bw.insert(run2, iHLen)
                    else:
                        if run2 < 8:
                            bw.insert(HORIZ_LONG_SHORT, 2)
                            bw.insert(run1, iHLen)
                            bw.insert(run2, 3)
                        else:
                            bw.insert(HORIZ_LONG_LONG, 2)
                            bw.insert(run1, iHLen)
                            bw.insert(run2, iHLen)

                    a0 = a2
                    if a0 != width:
                        iCur += 2
                        while iRef < len(ref_flips) and ref_flips[iRef] != width and ref_flips[iRef] <= a0:
                            iRef += 2
                else:
                    # Vertical mode
                    vi = dx + 3  # index 0..6
                    code, code_len = _vtable[vi]
                    bw.insert(code, code_len)
                    a0 = a1
                    if a0 != width:
                        if iRef != 0:
                            iRef -= 2
                        iRef += 1
                        iCur += 1
                        while iRef < len(ref_flips) and ref_flips[iRef] <= a0 and ref_flips[iRef] != width:
                            iRef += 2

        # Swap reference and current
        ref_flips = cur_flips

    bw.flush()
    return bytes(bw.buf)


def make_bb_bitmap(pixel_rows: list, width: int, height: int) -> bytes:
    """Create a complete BB_BITMAP blob (header + G5 data)."""
    g5_data = g5_encode(pixel_rows, width, height)
    # Header: marker(2) + width(2) + height(2) + compressed_size(2)
    header = struct.pack('<HHHH', BB_BITMAP_MARKER, width, height, len(g5_data))
    return header + g5_data


