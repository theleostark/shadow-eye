# Image rendering
# ---------------------------------------------------------------------------

def _find_font(size: int):
    """Try to find a clean monospace font, fall back to default."""
    font_paths = [
        "/System/Library/Fonts/Menlo.ttc",
        "/System/Library/Fonts/Courier.dfont",
        "/System/Library/Fonts/Monaco.dfont",
        "/usr/share/fonts/truetype/dejavu/DejaVuSansMono-Bold.ttf",
        "/usr/share/fonts/truetype/liberation/LiberationMono-Bold.ttf",
    ]
    for fp in font_paths:
        if os.path.exists(fp):
            try:
                return ImageFont.truetype(fp, size)
            except Exception:
                continue
    return ImageFont.load_default()


def render_echo_wordmark(width: int, height: int) -> Image.Image:
    """
    Render "ECHO" text centered on a black background (white text).
    For e-paper: white pixels = 1, black pixels = 0 in 1-bpp.
    """
    img = Image.new('1', (width, height), 0)  # black background
    draw = ImageDraw.Draw(img)

    # Find a good font size — target ~60% of the smaller dimension
    target_height = int(min(width, height) * 0.45)
    font_size = target_height
    font = _find_font(font_size)

    # Measure and adjust
    bbox = draw.textbbox((0, 0), "ECHO", font=font)
    text_w = bbox[2] - bbox[0]
    text_h = bbox[3] - bbox[1]

    # Scale font if text is too wide
    if text_w > width * 0.85:
        font_size = int(font_size * (width * 0.85) / text_w)
        font = _find_font(font_size)
        bbox = draw.textbbox((0, 0), "ECHO", font=font)
        text_w = bbox[2] - bbox[0]
        text_h = bbox[3] - bbox[1]

    x = (width - text_w) // 2 - bbox[0]
    y = (height - text_h) // 2 - bbox[1]

    draw.text((x, y), "ECHO", fill=1, font=font)
    return img


def render_qr_code(url: str, width: int, height: int) -> Image.Image:
    """Generate a QR code image at the specified dimensions."""
    qr = qrcode.QRCode(
        version=None,
        error_correction=qrcode.constants.ERROR_CORRECT_L,
        box_size=1,
        border=1,
    )
    qr.add_data(url)
    qr.make(fit=True)

    # qrcode library: white=255, black=0
    qr_img = qr.make_image(fill_color="black", back_color="white").convert('1')

    # Resize to target dimensions
    qr_img = qr_img.resize((width, height), Image.NEAREST)
    return qr_img


def image_to_pixel_rows(img: Image.Image) -> list:
    """
    Convert a PIL '1' mode image to list of bytes rows (MSB-first, 1-bpp).
    In the G5 format: white=1, black=0 (matching the original TRMNL bitmaps).
    """
    width, height = img.size
    row_bytes = (width + 7) // 8
    rows = []
    pixels = img.load()

    for y in range(height):
        row = bytearray(row_bytes)
        for x in range(width):
            if pixels[x, y]:  # white pixel = 1
                row[x >> 3] |= (0x80 >> (x & 7))
        rows.append(bytes(row))

    return rows


# ---------------------------------------------------------------------------
# C header generation
# ---------------------------------------------------------------------------

def format_c_header(var_name: str, data: bytes, width: int, height: int) -> str:
    """Format binary data as a C header file matching the original format."""
    compressed_size = len(data) - 8  # minus 8-byte header
    lines = []
    lines.append(HEADER_COMMENT)
    lines.append("//")
    lines.append(f"// {width} x {height} x 1-bit per pixel")
    lines.append(f"// compressed image data size = {len(data)} bytes")
    lines.append("//")
    lines.append(f"const uint8_t {var_name}[] = {{")

    # Format data bytes 16 per line
    for i in range(0, len(data), 16):
        chunk = data[i:i+16]
        hex_vals = ','.join(f'0x{b:02x}' for b in chunk)
        if i + 16 >= len(data):
            lines.append(hex_vals + '};')
        else:
            lines.append(hex_vals + ',')

    lines.append('')
    return '\n'.join(lines)


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

def main():
    OUTPUT_DIR.mkdir(parents=True, exist_ok=True)

    created_files = []

    # Generate logo bitmaps
    for name, spec in BITMAPS.items():
        w, h = spec['width'], spec['height']
        print(f"Generating {name} ({w}x{h})...")

        img = render_echo_wordmark(w, h)
        pixel_rows = image_to_pixel_rows(img)
        bb_data = make_bb_bitmap(pixel_rows, w, h)

        header_text = format_c_header(spec['var'], bb_data, w, h)
        out_path = OUTPUT_DIR / spec['file']
        out_path.write_text(header_text)

        created_files.append((out_path, len(bb_data)))
        print(f"  -> {out_path} ({len(bb_data)} bytes compressed)")

    # Generate QR code
    print(f"Generating wifi_failed_qr ({QR_SPEC['width']}x{QR_SPEC['height']})...")

    qr_img = render_qr_code(QR_SPEC['url'], QR_SPEC['width'], QR_SPEC['height'])
    pixel_rows = image_to_pixel_rows(qr_img)
    bb_data = make_bb_bitmap(pixel_rows, QR_SPEC['width'], QR_SPEC['height'])

    header_text = format_c_header(QR_SPEC['var'], bb_data, QR_SPEC['width'], QR_SPEC['height'])
    out_path = OUTPUT_DIR / QR_SPEC['file']
    out_path.write_text(header_text)

    created_files.append((out_path, len(bb_data)))
    print(f"  -> {out_path} ({len(bb_data)} bytes compressed)")

    # Summary
    print("\n--- Generated files ---")
    for path, size in created_files:
        file_size = path.stat().st_size
        print(f"  {path.name:25s}  {size:6d} bytes (data)  {file_size:6d} bytes (file)")

    print("\nDone.")


if __name__ == '__main__':
    main()
